        protected void Page_Load(object sender, EventArgs e)
        {
            if (!IsPostBack)
            {
                CargarAño();
                CargarPeriodo();
                CargarCliente();
                cmAño.Text = DateTime.Now.Year.ToString();
            }
        }
        public void CargarCliente()
        {
            cmCliente.Items.Clear();
            cmCliente.Items.Add(new ListItem("--Seleccione el Cliente--"));
            cmCliente.AppendDataBoundItems = true;
            ClaseConexion Conexion = new ClaseConexion();
            SqlConnection cnx = Conexion.conectar();
            SqlCommand cmd = new SqlCommand();
            try
            {
                //string sql = "SELECT Nombre +' '+ Apellidos As Nombre,Id_Persona FROM Persona where Id_Persona=3402 or Id_Persona=3403 or Id_Persona=3404 or Id_Persona=8828";
                string sql = "select Id_Cliente as Nombre from ConciliacionPorCobrar where idtipoContrato=3 group by Id_Cliente";
                cmd.CommandText = sql;
                cmd.Connection = cnx;
                SqlDataReader dr = null;
                cnx.Open();
                //cmd.Transaction = myTrans;
                dr = cmd.ExecuteReader();
                cmCliente.DataSource = dr; //cmd.ExecuteReader();    
                cmCliente.DataTextField = "Nombre";
                cmCliente.DataValueField = "Nombre";
                cmCliente.DataBind();
                dr.Close();
            }
            catch (Exception ex)
            {
                throw ex;
            }
            finally
            {
                cmd.Connection.Close();
                cmd.Connection.Dispose();
            }
        }
